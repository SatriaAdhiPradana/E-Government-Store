package project;

public class Departments {
	
	String dept_number;
	String dept_time;
	String main_office;
	
	
	public String getDept_number() {
		return dept_number;
	}
	public void setDept_number(String dept_number) {
		this.dept_number = dept_number;
	}
	public String getDept_time() {
		return dept_time;
	}
	public void setDept_time(String dept_time) {
		this.dept_time = dept_time;
	}
	public String getMain_office() {
		return main_office;
	}
	public void setMain_office(String main_office) {
		this.main_office = main_office;
	}
	
	
}
