package project;

public class Professors {
	
	String prof_ssn;
	String prof_name;
	String prof_age;
	String prof_gender;
	String prof_rank;
	String speciality;
	
	
	public String getProf_ssn() {
		return prof_ssn;
	}
	public void setProf_ssn(String prof_ssn) {
		this.prof_ssn = prof_ssn;
	}
	public String getProf_name() {
		return prof_name;
	}
	public void setProf_name(String prof_name) {
		this.prof_name = prof_name;
	}
	public String getProf_age() {
		return prof_age;
	}
	public void setProf_age(String prof_age) {
		this.prof_age = prof_age;
	}
	public String getProf_gender() {
		return prof_gender;
	}
	public void setProf_gender(String prof_gender) {
		this.prof_gender = prof_gender;
	}
	
	public String getProf_rank() {
		return prof_rank;
	}
	public void setProf_rank(String prof_rank) {
		this.prof_rank = prof_rank;
	}
	public String getSpeciality() {
		return speciality;
	}
	public void setSpeciality(String speciality) {
		this.speciality = speciality;
	}
	
	
	
	
	
	

}
