package project;

public class Students {
	
	String stu_ssn;
	String stu_name;
	String stu_age;
	String stu_gender;
	String degree_program;
	
	Projects projectId;
	
	
	public Projects getProjectId() {
		return projectId;
	}
	public void setProjectId(Projects projectId) {
		this.projectId = projectId;
	}
	public String getStu_ssn() {
		return stu_ssn;
	}
	public void setStu_ssn(String stu_ssn) {
		this.stu_ssn = stu_ssn;
	}
	public String getStu_name() {
		return stu_name;
	}
	public void setStu_name(String stu_name) {
		this.stu_name = stu_name;
	}
	public String getStu_age() {
		return stu_age;
	}
	public void setStu_age(String stu_age) {
		this.stu_age = stu_age;
	}
	public String getStu_gender() {
		return stu_gender;
	}
	public void setStu_gender(String stu_gender) {
		this.stu_gender = stu_gender;
	}
	public String getDegree_program() {
		return degree_program;
	}
	public void setDegree_program(String degree_program) {
		this.degree_program = degree_program;
	}
	
	

}
